class Model(nn.Module):
    def __init__(self, config):
        super(Model, self).__init__()
        if config.embedding_pretrained is not None:
            self.embedding = nn.Embedding.from_pretrained(config.embedding_pretrained, freeze=False)
        else:
            self.embedding = nn.Embedding(config.n_vocab, config.embed, padding_idx=config.n_vocab - 1)

        # 定义卷积层，配置不同的卷积核尺寸
        self.convs = nn.ModuleList(
            [nn.Conv2d(1, config.num_filters, (k, config.embed)) for k in config.filter_sizes])
        self.dropout = nn.Dropout(config.dropout)
        # 使用 KANLinear 层进行全连接层操作
        self.fc = KANLinear(config.num_filters * len(config.filter_sizes), config.num_classes)

    def conv_and_pool(self, x, conv):
        # 卷积和池化操作
        x = F.relu(conv(x)).squeeze(3)  # 去掉最后一个维度，即长度维度
        x = F.max_pool1d(x, x.size(2)).squeeze(2)  # 最大池化
        return x

    def forward(self, x):
        # 嵌入层输出形状 [batch_size, max_seq_len, embed_dim]
        out = self.embedding(x[0])  # x[0] 是输入文本的索引
        out = out.unsqueeze(1)  # 增加一个维度，变为 [batch_size, 1, max_seq_len, embed_dim]

        # 应用卷积层和池化层
        out = torch.cat([self.conv_and_pool(out, conv) for conv in self.convs], 1)
        out = self.dropout(out)  # Dropout
        out = self.fc(out)  # 全连接层输出
        return out